from __future__ import annotations

from collections.abc import Callable
from threading import Lock

from sqlalchemy import text

from app.celery_app import celery_app
from app.db.session import SessionLocal, engine
from app.evals.runner import run_eval_suite


_PROCESS_EVAL_LOCK = Lock()
_POSTGRES_EVAL_LOCK_KEY = 2_026_071_005


def _acquire_postgres_eval_lock() -> Callable[[], None] | None:
    """Serialize suites across worker processes on PostgreSQL.

    The dedicated connection keeps a transaction-scoped advisory lock for the
    full suite. It is intentionally separate from the eval session because
    results commit after every case. Ending this dedicated transaction releases
    the lock before SQLAlchemy can return the connection to its pool, including
    soft-timeout and exception paths. A hard-killed worker drops the connection,
    which PostgreSQL also treats as transaction termination.
    """
    if engine.dialect.name != "postgresql":
        return None

    connection = engine.connect()
    transaction = connection.begin()
    try:
        connection.execute(
            text("SELECT pg_advisory_xact_lock(:lock_key)"),
            {"lock_key": _POSTGRES_EVAL_LOCK_KEY},
        )
    except Exception:
        try:
            transaction.rollback()
        finally:
            connection.close()
        raise

    def release() -> None:
        try:
            transaction.rollback()
        finally:
            connection.close()

    return release


@celery_app.task(name="evals.run_suite")
def run_eval_suite_task(
    eval_run_id: str,
    dataset_id: str | None = None,
    agent_version_id: str | None = None,
) -> dict[str, object]:
    """Execute the eval suite synchronously within a Celery worker.

    The ``eval_run_id`` is pre-generated by the API route so the 202 response
    can advertise it before the task runs. Not retryable: a partial eval run
    would double-count scenarios or corrupt scoring, so fail-fast is safer
    than a blind retry. The Celery ``task_time_limit`` / ``task_soft_time_limit``
    guard against hangs.
    """
    # A/B suites investigate the same seeded incidents. Serialize them so the
    # active-incident uniqueness guard never turns worker scheduling into a
    # false evaluation failure. The process lock covers eager/unit execution;
    # the PostgreSQL advisory lock covers multiple Celery processes/hosts.
    with _PROCESS_EVAL_LOCK:
        release_postgres_lock = _acquire_postgres_eval_lock()
        try:
            with SessionLocal() as session:
                summary = run_eval_suite(
                    session,
                    eval_run_id=eval_run_id,
                    dataset_id=dataset_id,
                    agent_version_id=agent_version_id,
                )
        finally:
            if release_postgres_lock is not None:
                release_postgres_lock()

    return {
        "eval_run_id": summary.eval_run_id,
        "status": summary.status,
        "total_scenarios": summary.total_scenarios,
        "passed_scenarios": summary.passed_scenarios,
        "failed_scenarios": summary.failed_scenarios,
    }
