from __future__ import annotations

from app.celery_app import celery_app
from app.db.session import SessionLocal
from app.evals.runner import run_eval_suite


@celery_app.task(name="evals.run_suite")
def run_eval_suite_task(eval_run_id: str) -> dict[str, object]:
    """Execute the eval suite synchronously within a Celery worker.

    The ``eval_run_id`` is pre-generated by the API route so the 202 response
    can advertise it before the task runs. Not retryable: a partial eval run
    would double-count scenarios or corrupt scoring, so fail-fast is safer
    than a blind retry. The Celery ``task_time_limit`` / ``task_soft_time_limit``
    guard against hangs.
    """
    with SessionLocal() as session:
        summary = run_eval_suite(session, eval_run_id=eval_run_id)

    return {
        "eval_run_id": summary.eval_run_id,
        "status": summary.status,
        "total_scenarios": summary.total_scenarios,
        "passed_scenarios": summary.passed_scenarios,
        "failed_scenarios": summary.failed_scenarios,
    }
